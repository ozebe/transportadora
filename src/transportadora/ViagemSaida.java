    private void addViagemBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addViagemBotaoActionPerformed
        //pega a data que foi inserida no dataSaidaField e coloca na ArrayList viagens como o primeiro argumento do construtor do tipo Date
        //arrumar bug, quando a data tá errada ele continua a executar o código(parcialmente arrumado)
        Date dts;
        String dataSaida = dataSaidaField.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        try {
            dts = sdf.parse(dataSaida);
        } catch (ParseException pe) {
            JOptionPane.showMessageDialog(null, pe.getMessage() + "\nData inserida de forma incorreta", "Erro", JOptionPane.ERROR_MESSAGE);
            dts = null;
            
        }
        //----------------------------------------------------------------------------------------------------
        if(dts != null){
        String pesoString = pesoField.getText().replace(",", ".");
        String volumeString = volumeField.getText().replace(",", ".");
        double peso = Double.parseDouble(pesoString);
        double volume = Double.parseDouble(volumeString);

        //verificar motorista escolhido a partir do seletorDeMotorista----------------------------------------
        String nomeMotoristaSelecionado = (String) seletorDeMotorista.getSelectedItem();
        int posicaoMotoristaSelecionado = 0;
        for (int i = 0; i < AddMotorista.motoristas.size(); i++) {
            if (AddMotorista.motoristas.get(i).getNome().equals(nomeMotoristaSelecionado)) {
                posicaoMotoristaSelecionado = i;
            }

        }
        //----------------------------------------------------------------------------------------------------
        //verificar a existência de auxilaires que ajudaram na carga do veiculo escolhido---------------------
        
        //----------------------------------------------------------------------------------------------------
        Viagem viagem1 = new Viagem(dts, peso, volume, AddMotorista.motoristas.get(posicaoMotoristaSelecionado));
        viagens.add(viagem1);
        }

    }//GEN-LAST:event_addViagemBotaoActionPerformed

