    private void setorMotoristaBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setorMotoristaBotaoActionPerformed
        //procura o motorista baseado no que foi selecionado no seletorDeMotorista------------------
        try{
        String nomeMotoristaSelecionado = (String) seletorDeMotorista.getSelectedItem();
        int posicaoMotoristaSelecionado = 0;
        for (int i = 0; i < AddMotorista.motoristas.size(); i++) {
            if (AddMotorista.motoristas.get(i).getNome().equals(nomeMotoristaSelecionado)) {
                posicaoMotoristaSelecionado = i;
            }

        }
        //------------------------------------------------------------------------------------------
        if(AddMotorista.motoristas.get(posicaoMotoristaSelecionado).emViagem == false){
            AddMotorista.motoristas.get(posicaoMotoristaSelecionado).emViagem = true;
            motoristaLabel.setText("Motorista: " + AddMotorista.motoristas.get(posicaoMotoristaSelecionado).getNome());
        } else{
            JOptionPane.showMessageDialog(null, "O motorista selecionado já está em uma viagem!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        }catch(IndexOutOfBoundsException e){
            JOptionPane.showMessageDialog(null, "Necessita adicionar pelo menos um motorista", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_setorMotoristaBotaoActionPerformed

    

