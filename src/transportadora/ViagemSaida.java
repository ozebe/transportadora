    private void seletorDeEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seletorDeEstadoActionPerformed
        String nomeEstadoSelecionado = (String) seletorDeEstado.getSelectedItem();

        for (int n = 0; n < AddCidade.estado.size(); n++) {
            if (AddCidade.estado.get(n).getNome().equals(nomeEstadoSelecionado)) {
                seletorDeCidade.removeAllItems();
                int quantiaDeCidadesNoEstado = AddCidade.estado.get(n).tamanhoCidades();
                for (int j = 0; j < quantiaDeCidadesNoEstado; j++) {
                seletorDeCidade.addItem(AddCidade.estado.get(n).getCidades().get(j).getNome());

                }

            }
        }
    }//GEN-LAST:event_seletorDeEstadoActionPerformed

